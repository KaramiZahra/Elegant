// get the page id
let locationParams = new URLSearchParams(location.search);
let getID = locationParams.get("id");

// find the respective id's data
let findItem = itemsDB.find((item) => {
  return item.id == getID;
});

// create items for the given id
let itemContainer = document.querySelector(".item-container");
let header = document.querySelector("#header");

function createItem(item) {
  // insert header title
  header.insertAdjacentHTML(
    "beforeend",
    '<h1 class="title">' + item.title + '</h1><div class="underline"></div>'
  );

  // Iterate through the arrays for the current item
  for (let i = 0; i < item.itemName.length; i++) {
    itemContainer.insertAdjacentHTML(
      "beforeend",
      `
      <a href="#" class="item" id="${[i + 1]}">
        <img src="${item.imgSrc[i]}">
        <div class="overlay">View Details</div>
        <div class="item-desc">
          <p>${item.itemName[i]}</p>
          <p>${item.itemPrice[i]}$</p>
        </div>
      </a>
    `
    );
  }
}
createItem(findItem);
